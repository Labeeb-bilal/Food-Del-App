const { mongoose, model } = require('mongoose');

const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  cartData: {
    type: Object,
    default: {},
  },
}, { minimize: false });

const UserModel = mongoose.models.user || mongoose.model('user', UserSchema);

module.exports = UserModel;
